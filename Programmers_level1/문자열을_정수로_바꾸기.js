function solution(s) {
  // 문자열 s를 숫자로 변환한 결과를 반환하라
  // s의 길이는 1 이상 5 이하
  // s의 맨 앞에는 부호 (+, -)가 올 수 있다
  // s는 부호와 숫자로만 이루어져 있고 0으로 시작하지 않는다

  // ex) str = "1234" -> return 1234
  // ex) str = "-1234" -> return -1234

  return Number(s);
}
